// ✅ Switch Statement Example

let day = 1;

switch (day) {
  case 1:
    console.log("📅 Monday");
    break;
  case 2:
    console.log("📅 Tuesday");
    break;
  default:
    console.log("⚠️ Invalid Day");
    break;
}

// 📌 Use `switch` when comparing one variable to many fixed values.
// 📌 Use `if-else` when dealing with complex conditions or ranges.
