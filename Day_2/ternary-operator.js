// ✅ Ternary Operator

112 > 13 ? console.log("✅ Hello") : console.log("❌ Hey");

// Using ternary with result storage
let result = 112 > 13 ? "Hello" : "Hey";
console.log(result);

// ✅ Nested Ternary Operator

let num = 9;
let status = num > 0 ? "Positive" : num < 0 ? "Negative" : "Zero";
console.log("Number is:", status);
